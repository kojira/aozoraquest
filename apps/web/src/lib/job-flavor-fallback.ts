export function pickFallbackFlavor(archetype: Archetype, seed: number): string {
  const pool = JOB_FLAVOR_FALLBACKS[archetype];
  return pool[Math.abs(Math.floor(seed)) % pool.length] ?? pool[0]!;
}

export function pickFallbackEffect(archetype: Archetype, seed: number): string {
  const pool = JOB_EFFECT_FALLBACKS[archetype];
  return pool[Math.abs(Math.floor(seed)) % pool.length] ?? pool[0]!;
}
